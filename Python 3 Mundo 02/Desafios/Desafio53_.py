# O QUE É UM PALÍNDROMO?
#
# Um palíndromo é uma palavra ou uma frase
# que pode ser lida da esquerda para a direita
# e da direita para a esquerda,
# continuando exatamente igual.
#
# Para este desafio,
# os espaços não devem ser considerados.

# ------------------------------------------
# EXEMPLOS DE PALÍNDROMOS
# ------------------------------------------

# "OVO"

# Lendo ao contrário:

# OVO

# É igual.
#
# Portanto, é um palíndromo.

# ------------------------------------------

# "ARARA"

# Ao contrário:

# ARARA

# Continua igual.

# É um palíndromo.

# ------------------------------------------

# "OSSO"

# Ao contrário:

# OSSO

# Também é um palíndromo.

# ------------------------------------------

# "A SACADA DA CASA"

# Sem os espaços:

# ASACADADACASA

# Ao contrário:

# ASACADADACASA

# É igual.

# Portanto, é um palíndromo.

# ==========================================
# EXEMPLOS QUE NÃO SÃO PALÍNDROMOS
# ==========================================

# "PYTHON"

# Ao contrário:

# NOHTYP

# Ficou diferente.

# Não é um palíndromo.

# ------------------------------------------

# "COMPUTADOR"

# Ao contrário:

# RODATUPMOC

# Também ficou diferente.

# Não é um palíndromo.

