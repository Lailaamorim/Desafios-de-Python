# O QUE É UMA PA?
#
# PA significa Progressão Aritmética.
#
# É uma sequência de números em que
# a diferença entre um número e o próximo
# é sempre a mesma.
#
# Essa diferença é chamada de RAZÃO.

# ------------------------------------------
# EXEMPLO 1
# ------------------------------------------

# Primeiro termo: 2
# Razão: 3
#
# Sequência:
#
# 2
# 5
# 8
# 11
# 14
# 17
# 20
# 23
# 26
# 29
#
# Observe que sempre somamos 3.

# ------------------------------------------
# EXEMPLO 2
# ------------------------------------------

# Primeiro termo: 10
# Razão: 5
#
# Sequência:
#
# 10
# 15
# 20
# 25
# 30
# 35
# 40
# 45
# 50
# 55

# ------------------------------------------
# EXEMPLO 3
# ------------------------------------------

# Primeiro termo: 100
# Razão: -10
#
# Sequência:
#
# 100
# 90
# 80
# 70
# 60
# 50
# 40
# 30
# 20
# 10
#
# Quando a razão é negativa,
# a sequência vai diminuindo.

