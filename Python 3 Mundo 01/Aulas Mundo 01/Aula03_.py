# OPERADORES ARITMÉTICOS EM PYTHON
# --------------------------------
# Operadores aritméticos são símbolos usados para fazer cálculos matemáticos
# dentro do Python, como soma, subtração, multiplicação, etc.

# Eles servem para manipular números e são muito usados em programas,
# desde cálculos simples até sistemas mais complexos.

# PRINCIPAIS OPERADORES:
# +   -> soma
# -   -> subtração
# *   -> multiplicação
# /   -> divisão (resultado com casas decimais)
# //  -> divisão inteira (sem casas decimais)
# %   -> resto da divisão (módulo)
# **  -> potenciação (elevar um número ao outro)

# --------------------------------
# EXEMPLOS DE CADA OPERADOR:

# SOMA (+)
a = 10 + 5
print(a)  # resultado: 15

# SUBTRAÇÃO (-)
b = 10 - 5
print(b)  # resultado: 5

# MULTIPLICAÇÃO (*)
c = 10 * 5
print(c)  # resultado: 50

# DIVISÃO (/)
d = 10 / 5
print(d)  # resultado: 2.0 (sempre retorna float)

# DIVISÃO INTEIRA (//)
e = 10 // 3
print(e)  # resultado: 3 (ignora o resto)

# RESTO DA DIVISÃO (%)
f = 10 % 3
print(f)  # resultado: 1 (resto da divisão)

# POTENCIAÇÃO (**)
g = 2 ** 3
print(g)  # resultado: 8 (2 elevado a 3)

# --------------------------------
# EXEMPLOS MAIS PRÁTICOS:

# RAIZ QUADRADA (usando potenciação)
# raiz quadrada de 9
raiz = 9 ** 0.5
print(raiz)  # resultado: 3.0

# PORCENTAGEM (usando divisão)
# 20% de 100
porcentagem = 100 * 0.20
print(porcentagem)  # resultado: 20.0

# --------------------------------
# RESUMO:
# Use esses operadores para fazer qualquer tipo de cálculo no Python.
# Eles são básicos, mas MUITO importantes para programação.